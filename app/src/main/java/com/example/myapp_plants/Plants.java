package com.example.myapp_plants;

public class Plants {
    private String tj_name;
    private String rus_name;
    private String eng_name;
    private String notes;
    private String image;

    public String getTj_name() {
        return tj_name;
    }

    public void setTj_name(String tj_name) {
        this.tj_name = tj_name;
    }

    public String getRus_name() {
        return rus_name;
    }

    public void setRus_name(String rus_name) {
        this.rus_name = rus_name;
    }

    public String getEng_name() {
        return eng_name;
    }

    public void setEng_name(String eng_name) {
        this.eng_name = eng_name;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
